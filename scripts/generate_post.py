#!/usr/bin/env python3
"""
Blogger Auto-Poster with AI Images – Uses GitHub Secrets
- Reads credentials from environment variables.
- Generates a trending topic post using Ollama (tinyllama).
- Adds a free AI‑generated image from Pollinations.ai.
- Publishes directly to Blogger via OAuth2.
- Saves a local .md backup and logs all errors.
"""

import os
import sys
import requests
import feedparser
import random
import subprocess
import traceback
import urllib.parse
from datetime import datetime
from pathlib import Path

# Google API imports
from google.oauth2.credentials import Credentials
from googleapiclient.discovery import build
from google.auth.transport.requests import Request
from google.auth.exceptions import RefreshError
from googleapiclient.errors import HttpError

# ==================== READ SECRETS FROM ENVIRONMENT ====================
BLOGGER_BLOG_ID = os.getenv("BLOGGER_BLOG_ID")
GOOGLE_CLIENT_ID = os.getenv("GOOGLE_CLIENT_ID")
GOOGLE_CLIENT_SECRET = os.getenv("GOOGLE_CLIENT_SECRET")
GOOGLE_REFRESH_TOKEN = os.getenv("GOOGLE_REFRESH_TOKEN")

# ==================== SETUP DIRECTORIES ====================
CACHE_DIR = Path(".blog-cache")
POSTS_DIR = Path("_posts")
CACHE_DIR.mkdir(exist_ok=True)
POSTS_DIR.mkdir(exist_ok=True)

# ==================== LOGGING HELPER ====================
def log_error(step, error, details=None):
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    print(f"\n❌ ERROR at {timestamp}")
    print(f"   Step: {step}")
    print(f"   Error: {error}")
    if details:
        print(f"   Details: {details}")
    print(f"   Traceback: {traceback.format_exc()}")

# ==================== POLLINATIONS.AI IMAGE ====================
def get_pollinations_image(topic_title, width=1024, height=576):
    """Get a free AI‑generated image URL from Pollinations.ai."""
    try:
        prompt = f"Professional blog header image about {topic_title}, high quality, 4k, photorealistic"
        encoded = urllib.parse.quote(prompt)
        # Use a random seed for variety
        image_url = f"https://pollinations.ai/p/{encoded}?width={width}&height={height}&seed={random.randint(1,10000)}&nologo=true"
        return image_url
    except Exception as e:
        log_error("Image Generation", str(e))
        return None

def create_image_html(title):
    """Generate HTML for the image at the top of the post."""
    url = get_pollinations_image(title)
    if url:
        return f'''
        <div style="margin-bottom:30px; text-align:center;">
            <img src="{url}" alt="{title}"
                 style="width:100%; max-width:800px; border-radius:12px; box-shadow:0 4px 20px rgba(0,0,0,0.15);">
            <p style="color:#777; font-size:0.8em;">🎨 AI-generated by Pollinations.ai</p>
        </div>
        '''
    return ''

# ==================== BLOGGER AUTHENTICATION ====================
def get_blogger_service():
    """Obtain an authenticated Blogger service object."""
    # Check that all required credentials are present
    if not all([GOOGLE_CLIENT_ID, GOOGLE_CLIENT_SECRET, GOOGLE_REFRESH_TOKEN]):
        print("❌ Missing one or more Google credentials in environment.")
        return None

    try:
        creds = Credentials(
            token=None,
            refresh_token=GOOGLE_REFRESH_TOKEN,
            token_uri="https://oauth2.googleapis.com/token",
            client_id=GOOGLE_CLIENT_ID,
            client_secret=GOOGLE_CLIENT_SECRET,
            scopes=["https://www.googleapis.com/auth/blogger"]
        )
        print("🔄 Refreshing access token...")
        creds.refresh(Request())
        print("✅ Token refreshed successfully.")
        service = build('blogger', 'v3', credentials=creds)

        # Verify the blog exists (optional but helpful)
        blog_info = service.blogs().get(blogId=BLOGGER_BLOG_ID).execute()
        print(f"✅ Blog verified: {blog_info.get('name', 'Unknown')}")
        return service

    except RefreshError as e:
        log_error("Token Refresh", str(e),
                  "Refresh token expired/revoked. Generate a new one from OAuth Playground.")
        print("\n🔑 SOLUTION:")
        print("   1. Go to https://developers.google.com/oauthplayground")
        print("   2. Click gear icon → Use your own OAuth credentials")
        print("   3. Enter your Client ID and Secret")
        print("   4. Ensure Redirect URI is exactly: https://developers.google.com/oauthplayground")
        print("   5. Select ONLY the Blogger API v3 scope")
        print("   6. Authorize and exchange for a new refresh token")
        print("   7. Update the GOOGLE_REFRESH_TOKEN secret in GitHub")
        return None
    except Exception as e:
        log_error("Authentication", str(e))
        return None

# ==================== POST TO BLOGGER ====================
def post_to_blogger(title, content, labels=None):
    """Publish the post to Blogger with an image."""
    if labels is None:
        labels = ['AI Generated', 'Trending']

    service = get_blogger_service()
    if not service:
        return False, "Authentication failed"

    # Add the image HTML at the top
    image_html = create_image_html(title)
    full_content = image_html + content.replace('\n', '<br>')

    post_body = {
        "kind": "blogger#post",
        "title": title,
        "content": f"""
        <div style="font-family:Arial,sans-serif; line-height:1.8; max-width:800px; margin:0 auto;">
            {full_content}
            <hr>
            <p style="color:#666; font-style:italic;">Auto-generated on {datetime.now().strftime('%B %d, %Y')}</p>
        </div>
        """,
        "labels": labels
    }

    try:
        response = service.posts().insert(blogId=BLOGGER_BLOG_ID, body=post_body).execute()
        post_url = response.get('url')
        print(f"✅ Post published: {post_url}")
        return True, post_url
    except HttpError as e:
        status = e.resp.status
        log_error("Blogger API HTTP Error", str(e), f"HTTP {status}")
        if status == 401:
            print("🔑 Token invalid – need new refresh token.")
        elif status == 403:
            print("🔒 Permission denied – check blog ownership.")
        elif status == 404:
            print("🔍 Blog ID not found – verify BLOGGER_BLOG_ID.")
        return False, str(e)
    except Exception as e:
        log_error("Blogger API", str(e))
        return False, str(e)

# ==================== FETCH TRENDING TOPICS ====================
def get_trending_topics():
    """Retrieve trending topics from several RSS feeds; fallback if empty."""
    topics = []
    sources = [
        ('https://news.ycombinator.com/rss', 'Hacker News', 3),
        ('http://feeds.bbci.co.uk/news/rss.xml', 'BBC', 2),
        ('https://techcrunch.com/feed/', 'TechCrunch', 2),
    ]

    for url, name, limit in sources:
        try:
            feed = feedparser.parse(url)
            for entry in feed.entries[:limit]:
                if entry.title and '[Removed]' not in entry.title:
                    topics.append({
                        'title': entry.title,
                        'description': entry.get('summary', '')[:200],
                        'source': name
                    })
        except Exception as e:
            log_error(f"RSS Feed {name}", str(e))

    # Fallback topics if everything fails
    if not topics:
        topics = [
            {'title': 'The Future of Artificial Intelligence',
             'description': 'How AI is transforming our daily lives', 'source': 'Tech'},
            {'title': 'Climate Tech Innovations',
             'description': 'Breakthroughs in renewable energy', 'source': 'Science'},
            {'title': 'Space Exploration Updates',
             'description': 'New missions to the Moon and Mars', 'source': 'Space'}
        ]
    return topics

# ==================== GENERATE CONTENT WITH OLLAMA ====================
def generate_with_ollama(prompt, model="tinyllama"):
    """Generate text using Ollama (API first, then CLI fallback)."""
    # Try API
    try:
        resp = requests.post('http://localhost:11434/api/generate',
                              json={"model": model, "prompt": prompt, "stream": False,
                                    "options": {"temperature": 0.7, "num_predict": 800}},
                              timeout=180)
        if resp.status_code == 200:
            content = resp.json().get('response', '').strip()
            if content:
                return content
    except Exception as e:
        print(f"⚠️ Ollama API error: {e}")

    # Fallback to CLI (using absolute path)
    try:
        result = subprocess.run(['/usr/local/bin/ollama', 'run', model, prompt],
                                capture_output=True, text=True, timeout=300)
        if result.returncode == 0:
            return result.stdout.strip()
    except Exception as e:
        print(f"⚠️ Ollama CLI error: {e}")

    # Ultimate fallback content
    return f"""
    <h2>Introduction</h2>
    <p>Today we're discussing {prompt[:50]}... This topic has been generating interest recently.</p>
    <h2>Key Points</h2>
    <p>Staying informed helps us understand the world around us. We'll continue to monitor developments.</p>
    <h2>Conclusion</h2>
    <p>Thank you for reading this automated post. More updates coming soon!</p>
    """

def generate_blog_post(topic):
    """Generate the full blog post content."""
    prompt = f"""Write a detailed blog post about:
Title: {topic['title']}
Description: {topic['description']}
Source: {topic['source']}

Write 300-400 words with an engaging introduction, 2-3 informative paragraphs, and a strong conclusion.
Make it professional and interesting. Use proper paragraphs.
"""
    return generate_with_ollama(prompt)

# ==================== LOCAL BACKUP ====================
def save_local_post(title, content):
    """Save a local copy of the post as a Markdown file."""
    slug = title.lower().replace(' ', '-')[:50]
    slug = ''.join(c for c in slug if c.isalnum() or c == '-')
    filename = POSTS_DIR / f"{datetime.now().strftime('%Y-%m-%d')}-{slug}.md"
    with open(filename, 'w', encoding='utf-8') as f:
        f.write(f"# {title}\n\n{content}")
    print(f"💾 Local backup saved: {filename}")
    return filename

# ==================== MAIN EXECUTION ====================
def main():
    print("=" * 60)
    print("🚀 AI BLOGGER – Secrets Version")
    print(f"📅 {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}")
    print("=" * 60)

    # Verify that all required secrets are present
    missing = []
    if not BLOGGER_BLOG_ID:
        missing.append("BLOGGER_BLOG_ID")
    if not GOOGLE_CLIENT_ID:
        missing.append("GOOGLE_CLIENT_ID")
    if not GOOGLE_CLIENT_SECRET:
        missing.append("GOOGLE_CLIENT_SECRET")
    if not GOOGLE_REFRESH_TOKEN:
        missing.append("GOOGLE_REFRESH_TOKEN")
    if missing:
        print(f"❌ Missing environment variables: {', '.join(missing)}")
        sys.exit(1)
    print("✅ All credentials found in environment.")

    # Get a trending topic
    topics = get_trending_topics()
    if not topics:
        print("❌ No topics available. Exiting.")
        sys.exit(1)

    chosen = random.choice(topics)
    print(f"\n🎯 Selected topic: {chosen['title']} (from {chosen['source']})")

    # Generate content
    print("\n✍️ Generating blog post with Ollama...")
    content = generate_blog_post(chosen)
    print(f"✅ Content generated ({len(content)} characters)")

    # Save local backup
    local_file = save_local_post(chosen['title'], content)

    # Post to Blogger
    print("\n📤 Posting to Blogger...")
    success, result = post_to_blogger(
        chosen['title'],
        content,
        labels=['AI Generated', chosen['source'].replace(' ', '-'), 'Pollinations']
    )

    # Final status
    print("\n" + "=" * 60)
    if success:
        print("✨✨✨ SUCCESS! Blog post published.")
        print(f"📌 URL: {result}")
    else:
        print("⚠️⚠️⚠️ PARTIAL SUCCESS")
        print(f"❌ Blogger posting failed: {result}")
        print(f"✅ Local backup available at: {local_file}")
    print("=" * 60)

if __name__ == "__main__":
    try:
        main()
    except KeyboardInterrupt:
        print("\n\n⚠️ Interrupted by user.")
        sys.exit(0)
    except Exception as e:
        log_error("Main execution", str(e))
        sys.exit(1)
