def post_to_blogger(title, content, labels=None):
    """Publish post to Blogger with comprehensive error handling."""
    if labels is None:
        labels = ['AI Generated', 'Trending']
    
    print("\n" + "="*40)
    print("📤 ATTEMPTING TO POST TO BLOGGER")
    print("="*40)
    
    # Step 1: Get authenticated service
    print("\n🔄 Step 1: Getting Blogger service...")
    service = get_blogger_service()
    if not service:
        error_msg = "Failed to get Blogger service - check authentication"
        print(f"❌ {error_msg}")
        return False, error_msg
    
    # Step 2: Add image at the top
    print("\n🔄 Step 2: Adding AI image...")
    image_html = create_image_html(title)
    if image_html:
        print("✅ Image HTML created")
    else:
        print("⚠️ No image generated, continuing without image")
    
    # Step 3: Format content
    print("\n🔄 Step 3: Formatting post content...")
    full_content = image_html + content.replace('\n', '<br>')
    
    post_body = {
        "kind": "blogger#post",
        "title": title,
        "content": f"""
        <div style="font-family: Arial, sans-serif; line-height:1.8; max-width:800px; margin:0 auto;">
            {full_content}
            <hr style="margin:40px 0 20px;">
            <p style="color:#666; font-style:italic;">Auto-generated on {datetime.now().strftime('%B %d, %Y at %H:%M UTC')}</p>
        </div>
        """,
        "labels": labels
    }
    print("✅ Content formatted")
    
    # Step 4: Send to Blogger API
    print("\n🔄 Step 4: Sending to Blogger API...")
    try:
        posts = service.posts()
        request = posts.insert(blogId=BLOGGER_BLOG_ID, body=post_body)
        print("⏳ Waiting for response...")
        
        response = request.execute()
        
        if response and 'url' in response:
            print(f"\n✅✅✅ SUCCESS! Post published!")
            print(f"📌 URL: {response.get('url')}")
            print(f"📌 Post ID: {response.get('id')}")
            return True, response.get('url')
        else:
            error_msg = "API returned success but no URL"
            print(f"⚠️ {error_msg}")
            return False, error_msg
            
    except HttpError as e:
        status = e.resp.status if hasattr(e, 'resp') else 'Unknown'
        error_details = f"HTTP {status}: {str(e)}"
        log_error("Blogger API HTTP Error", error_details)
        
        # Specific error codes
        if status == 401:
            print("\n🔑 SOLUTION: Token unauthorized. Need new refresh token.")
        elif status == 403:
            print("\n🔒 SOLUTION: Permission denied. Check blog ownership.")
        elif status == 404:
            print("\n🔍 SOLUTION: Blog ID not found. Check BLOGGER_BLOG_ID.")
        elif status == 429:
            print("\n⏳ SOLUTION: Rate limited. Wait and try again.")
            
        return False, error_details
        
    except Exception as e:
        log_error("Blogger API", str(e))
        return False, str(e)

